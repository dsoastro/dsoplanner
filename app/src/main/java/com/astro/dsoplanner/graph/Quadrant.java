    public double cosDist(double ra, double dec) {
        double cosd;
        cosd = Point.tfun.sin((dec1 + dec2) / 2 * PI / 180) * Point.tfun.sin(dec * PI / 180) + Point.tfun.cos((dec1 + dec2) / 2 * PI / 180) * Point.tfun.cos(dec * PI / 180) * Point.tfun.cos(((ra1 + ra2) / 2 - ra) * PI / 12);
        return cosd;
    }

    private double minDist(double ra, double dec) {
        double cosd;
        double cosd1;
        cosd = sin(dec1 * PI / 180) * sin(dec * PI / 180) + cos(dec1 * PI / 180) * cos(dec * PI / 180) * cos((ra1 - ra) * PI / 12);
        cosd1 = sin(dec1 * PI / 180) * sin(dec * PI / 180) + cos(dec1 * PI / 180) * cos(dec * PI / 180) * cos((ra2 - ra) * PI / 12);
        if (cosd1 < cosd) cosd = cosd1;
        cosd1 = sin(dec2 * PI / 180) * sin(dec * PI / 180) + cos(dec2 * PI / 180) * cos(dec * PI / 180) * cos((ra2 - ra) * PI / 12);
        if (cosd1 < cosd) cosd = cosd1;
        cosd1 = sin(dec2 * PI / 180) * sin(dec * PI / 180) + cos(dec2 * PI / 180) * cos(dec * PI / 180) * cos((ra1 - ra) * PI / 12);
        if (cosd1 < cosd) cosd = cosd1;
        return cosd;
    }

    public Quadrant(double ra1, double ra2, double dec1, double dec2) {
        if (ra2 > ra1) {
            this.ra1 = (float) ra1;
            this.ra2 = (float) ra2;
        } else {
            this.ra1 = (float) ra2;
            this.ra2 = (float) ra1;
        }
        if (dec2 > dec1) {
            this.dec1 = (float) dec1;
            this.dec2 = (float) dec2;
        } else {
            this.dec1 = (float) dec2;
            this.dec2 = (float) dec1;
        }


    }

    public boolean inQuadrant(double ra, double dec) { //put >=!!!
        if (dec > 90) {
            dec = 180 - dec;
            ra = ra + 12;
        }
        if (dec < -90) {
            dec = -180 - dec;
            ra = ra + 12;
        }

        ra = AstroTools.normalise24(ra);
        return (ra >= ra1) && (ra < ra2) && (dec >= dec1) && (dec < dec2);
    }

    public boolean inQuadrant(Point p) {
        return inQuadrant(p.getRa(), p.getDec());
    }
