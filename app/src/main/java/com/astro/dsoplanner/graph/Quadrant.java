    public static List<Quadrant> makeQuadrants(int numY) {//90
        List<Quadrant> list = new ArrayList<Quadrant>();
        int numX = 4 * numY;
        double dec = 0;
        double sq = (cos(dec) + cos(dec + PI / 2 / numY)) / numX;
        for (int i = 0; i < numY; i++) {
            double s;
            while ((s = (cos(dec) + cos(dec + PI / 2 / numY)) / numX) < sq) {
                numX--;
            }
            for (int k = 0; k < numX; k++) {
                Quadrant q = new Quadrant(k * 24.0 / numX, (k + 1) * 24.0 / numX, dec * 180 / PI, (dec + PI / 2 / numY) * 180 / PI);
                list.add(q);
                Quadrant q1 = new Quadrant(k * 24.0 / numX, (k + 1) * 24.0 / numX, -dec * 180 / PI, -(dec + PI / 2 / numY) * 180 / PI);
                list.add(q1);
            }
            dec = dec + PI / 2 / numY;
        }
        return list;
    }


