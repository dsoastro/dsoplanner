package com.astro.dsoplanner.pushcamera;

public class P3s {
    int x, y;
    double z;

    public P3s(int x, int y, double z) {
        super();
        this.x = x;
        this.y = y;
        this.z = z;
    }

    @Override
    public String toString() {
        return "P3s [x=" + x + ", y=" + y + ", z=" + z + "]";
    }

}
